public class variable {
    public static void main(String args[]) {
        int num = 5;
        double percent = 6.5;
        float marks = 7.5f;
        short small = 5;
        byte very = 6;
        long big = 5000;
        System.out.println(num);
        System.out.println(percent);
        System.out.println(marks);
        System.out.println(small);
        System.out.println(very);
        System.out.println(big);

    }
}